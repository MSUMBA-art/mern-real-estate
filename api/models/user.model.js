import mongoose from "mongoose";

// CREATING USER 
const userSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    unique: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
}, { timestamps: true });

// CREATING MODEL

const user = mongoose.model("User", userSchema);

export default user